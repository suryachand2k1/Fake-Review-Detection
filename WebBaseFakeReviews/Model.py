
class Model:    

    def setTFIDF(self,tfidf):
        self.tfidf = tfidf

    def getTFIDF(self):
        return self.tfidf

    def setEMSVM(self,emsvm):
        self.emsvm = emsvm

    def getEMSVM(self):
        return self.emsvm

    def setEMNaiveBayes(self,emnb):
        self.emnb = emnb

    def getEMNaiveBayes(self):
        return self.emnb

    def setSVM(self,svm):
        self.svm = svm

    def getSVM(self):
        return self.svm

    def setNaiveBayes(self,nb):
        self.nb = nb

    def getNaiveBayes(self):
        return self.nb
        
        
